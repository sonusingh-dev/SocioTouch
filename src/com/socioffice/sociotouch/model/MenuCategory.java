package com.socioffice.sociotouch.model;

public class MenuCategory {
	
	private int id;
	private char ch;
	private String name;
	
	public MenuCategory() {
		
	}

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the ch
	 */
	public char getCh() {
		return ch;
	}

	/**
	 * @param ch the ch to set
	 */
	public void setCh(char ch) {
		this.ch = ch;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
